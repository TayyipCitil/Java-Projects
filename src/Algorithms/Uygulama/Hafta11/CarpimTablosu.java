package Algorithms.Uygulama.Hafta11;

public class CarpimTablosu {
    public static void main(String[] args) {
        // Herhangi bir n değerine göre çarpım tablosunu oluşturun
       int n = 6;
        for (int i = 1; i < 10 ; i++) {
            System.out.println(n + "x" + i+ "="+ (n*i));
        }

    }
}
